# –ú–Ķ–Ĺ—Ź–Ķ–ľ –∑–į–≥–ĺ–Ľ–ĺ–≤–ĺ–ļ –ĺ–ļ–Ĺ–į (–ĺ–Ĺ –≤—Ā—Ď —Ä–į–≤–Ĺ–ĺ —Ā–ļ—Ä—č—ā, –Ĺ–ĺ –Ņ—É—Ā—ā—Ć –Ī—É–ī–Ķ—ā)
